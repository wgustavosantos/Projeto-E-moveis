    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
       String tipo_imovel = cmbTipo_imovel.getSelectedItem().toString();
        int area_imovel = Integer.parseInt(txtArea_imovel.getText());
        String cor_imovel = txtCor_imovel.getText();
        int qtd_comodos_imovel = Integer.parseInt(txtQtd_comodos_imovel.getText());
        String endereco_imovel = txtEndereco_imovel.getText() + ",";
        endereco_imovel += txtEnderecobairro_imovel.getText() + ",";
        endereco_imovel += txtEndereconumero_imovel.getText() + ",";
        endereco_imovel += txtEnderecocep_imovel.getText() + ",";
        endereco_imovel += txtEnderecocidade_imovel.getText();
        double valor_imovel = Double.parseDouble(txtValor_imovel.getText());
        boolean status_imovel = (cmbStatus_imovel.getSelectedItem().toString().equals("Disponivel")) ;
        int id_imovel = this.id_imovel;
        
        Imovel i = new Imovel.ImovelBuilder()
                .tipo_imovel(tipo_imovel)
                .area_imovel(area_imovel)
                .cor_imovel(cor_imovel)
                .qtd_comodos_imovel(qtd_comodos_imovel)
                .endereco_imovel(endereco_imovel)
                .valor_imovel(valor_imovel)
                .status_imovel(status_imovel)
                .id_imovel(id_imovel)
                .build();
        
        ImovelCRUD.update(i);
       
        cmbTipo_imovel.setSelectedIndex(0);
        txtArea_imovel.setText("");
        txtCor_imovel.setText("");
        txtQtd_comodos_imovel.setText("");
        txtEndereco_imovel.setText("");
        txtEnderecobairro_imovel.setText("");
        txtEnderecocep_imovel.setText("");
        txtEnderecocidade_imovel.setText("");
        txtEndereconumero_imovel.setText("");
        txtValor_imovel.setText("");
        cmbStatus_imovel.setSelectedIndex(0);
        
        JOptionPane.showMessageDialog(null, "Atualizado com sucesso");
        
        ListaImovel listaImovel = new ListaImovel();
        listaImovel.setVisible(true);
        
        this.dispose();
        
    }//GEN-LAST:event_btnAtualizarActionPerformed

