     public int capturarId(int id_imovel){
         
         Imovel i = ImovelCRUD.readSIngle(id_imovel);
         
         int indexTipo_imovel = (i.getTipo_imovel().equalsIgnoreCase("Casa")) ? 1 : 2;
         int indexStatus_imovel = (i.isStatus_imovel()) ? 1 : 2; 
         
         
         cmbTipo_imovel.setSelectedIndex(indexTipo_imovel);
         txtArea_imovel.setText(String.valueOf(i.getArea_imovel()));
         txtCor_imovel.setText(i.getCor_imovel());
         txtQtd_comodos_imovel.setText(String.valueOf(i.getQtd_comodos_imovel()));
         
         
         String endereco_imovel = i.getEndereco_imovel();

            String enderecos[] = endereco_imovel.split(",");
            endereco_imovel = enderecos[0];
            
            String enderecoBairro_imovel = enderecos[1];
            String enderecoNumero_imovel = enderecos[2];
            String enderecoCep_imovel = enderecos[3];
            String enderecoCidade_imovel = enderecos[4];
         
         txtEndereco_imovel.setText(endereco_imovel);
        txtEnderecobairro_imovel.setText(enderecoBairro_imovel);
        txtEndereconumero_imovel.setText(enderecoNumero_imovel);
        txtEnderecocep_imovel.setText(enderecoCep_imovel);
        txtEnderecocidade_imovel.setText(enderecoCidade_imovel);
        txtValor_imovel.setText(String.valueOf(i.getValor_imovel()));
        cmbStatus_imovel.setSelectedIndex(indexStatus_imovel);
         
        return id_imovel;
        
         
     }
     
     
